'''
Escribir un programa que permita ingresar un numero entero.
Debe mostrarse el numero ingresado:
- Multiplicado por 10.
- Dividio por 10.
- Elevado al cuadrado.
Cada resultado debe mostrase en una linea distinta.
'''

number = int(input("Ingrese un numero: "))
print(f"{number} * 10 = {number * 10}")
print(f"{number} / 10 = {number / 10}")
print(f"{number} ** 2 = {number ** 2}")